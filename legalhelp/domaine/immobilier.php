    <h1>Droit de l'Immobilier</h1>
    <p>Extraits importants du Code de l’immobilier pour achat, vente, location et copropriété</p>

    <article>
      <h2>Article 1er - Champ d’application</h2>
      <p class="article-content">
        Le présent code régit les rapports juridiques liés à la propriété immobilière, à la location, à la copropriété, ainsi qu’aux transactions immobilières.
      </p>
    </article>

    <article>
      <h2>Article 1708 - Vente d’un bien immobilier</h2>
      <p class="article-content">
        La vente d’un bien immobilier est un contrat par lequel le vendeur transfère la propriété du bien à l’acheteur contre un prix convenu. Ce contrat doit être écrit et signé par les parties.
      </p>
    </article>

    <article>
      <h2>Article 1713 - Obligations du bailleur</h2>
      <p class="article-content">
        Le bailleur est tenu de délivrer un logement en bon état d’usage et de réparation, ainsi que d’assurer la jouissance paisible des lieux loués.
      </p>
    </article>

    <article>
      <h2>Article 1er de la loi du 10 juillet 1965 - Copropriété</h2>
      <p class="article-content">
        La copropriété est un régime juridique applicable aux immeubles bâtis divisés en lots destinés à l’habitation ou à l’usage professionnel, comprenant une partie privative et une partie commune.
      </p>
    </article>

