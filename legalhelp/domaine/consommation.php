    <h1>Droit de la Consommation</h1>
    <p>Extraits sélectionnés du Code de la consommation pour protéger les consommateurs et encadrer les litiges</p>

    <article>
      <h2>Article L111-1 - Information du consommateur</h2>
      <p class="article-content">
        Le professionnel doit, avant la conclusion du contrat, fournir au consommateur une information claire, compréhensible et non trompeuse sur les caractéristiques essentielles du bien ou du service, son prix, les modalités de paiement, de livraison et d'exécution, ainsi que les garanties légales et commerciales.
      </p>
    </article>

    <article>
      <h2>Article L121-1 - Pratiques commerciales déloyales</h2>
      <p class="article-content">
        Sont interdites les pratiques commerciales trompeuses ou agressives qui altèrent de manière significative le comportement économique du consommateur moyen, notamment la publicité mensongère, la dissimulation d'informations importantes, ou les pressions commerciales excessives.
      </p>
    </article>

    <article>
      <h2>Article L221-18 - Droit de rétractation</h2>
      <p class="article-content">
        Le consommateur dispose d'un délai de 14 jours calendaires pour exercer son droit de rétractation sans avoir à justifier de motifs ni à payer de pénalités, à compter de la réception du bien ou de la conclusion du contrat de service.
      </p>
    </article>

    <article>
      <h2>Article L217-4 - Garantie légale de conformité</h2>
      <p class="article-content">
        Le vendeur est tenu de livrer un bien conforme au contrat et répond des défauts de conformité existant lors de la délivrance. Le consommateur peut choisir entre la réparation, le remplacement du bien ou, à défaut, la réduction du prix ou la résolution du contrat.
      </p>
    </article>

