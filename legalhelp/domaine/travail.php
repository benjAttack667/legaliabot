      <h2>Article L1111-1</h2>
      <p class="article-content">
        Les dispositions du présent livre sont applicables aux employeurs de droit privé ainsi qu'à leurs salariés.<br><br>
        Elles sont également applicables au personnel des personnes publiques employé dans les conditions du droit privé, sous réserve des dispositions particulières ayant le même objet résultant du statut qui régit ce personnel.
      </p>

      <h2>Article L1111-2</h2>
      <p class="article-content">
        <strong>Modifié par LOI n°2008-789 du 20 août 2008 - art. 3</strong><br><br>
        Pour la mise en œuvre des dispositions du présent code, les effectifs de l'entreprise sont calculés conformément aux dispositions suivantes :<br><br>
        1° Les salariés titulaires d'un contrat de travail à durée indéterminée à temps plein et les travailleurs à domicile sont pris intégralement en compte dans l'effectif de l'entreprise ;<br><br>
        2° Les salariés titulaires d'un contrat de travail à durée déterminée, les salariés titulaires d'un contrat de travail intermittent, les salariés mis à la disposition de l'entreprise par une entreprise extérieure présents depuis au moins un an, ainsi que les salariés temporaires, sont pris en compte au prorata de leur temps de présence au cours des douze mois précédents.<br>
        Toutefois, sont exclus ceux qui remplacent un salarié absent ou en congé parental, maternité, adoption, etc. ;<br><br>
        3° Les salariés à temps partiel sont comptabilisés selon la proportion de leurs horaires contractuels par rapport à la durée légale ou conventionnelle.
      </p>

      <h2>Article L1111-3</h2>
      <p class="article-content">
        <strong>Modifié par ORDONNANCE n°2015-1578 du 3 décembre 2015 - art. 1</strong><br><br>
        Ne sont pas pris en compte dans le calcul des effectifs de l'entreprise :<br><br>
        1° Les apprentis ;<br>
        2° Les titulaires d'un contrat initiative-emploi, pendant la durée d'attribution de l'aide ;<br>
        4° Les titulaires d'un contrat d'accompagnement dans l'emploi ;<br>
        6° Les titulaires d'un contrat de professionnalisation.<br><br>
        Toutefois, ils sont pris en compte pour la tarification des risques professionnels.
      </p>

