    <h1>Droit de la Famille</h1>
    <p>Extraits essentiels du Code civil pour le divorce, la garde d’enfants et les successions</p>

    <article>
      <h2>Article 229 - Divorce par consentement mutuel</h2>
      <p class="article-content">
        Le divorce par consentement mutuel est possible lorsque les époux s'accordent sur la rupture du mariage et ses conséquences, sans passer par un juge. Ils doivent déposer une convention de divorce chez un notaire.
      </p>
    </article>

    <article>
      <h2>Article 373-2 - Autorité parentale et résidence de l’enfant</h2>
      <p class="article-content">
        En cas de séparation des parents, l’enfant a le droit d’entretenir des relations personnelles avec chacun de ses parents. La résidence de l’enfant peut être fixée chez l’un des parents ou alternée entre les deux, dans l’intérêt supérieur de l’enfant.
      </p>
    </article>

    <article>
      <h2>Article 720 - Ouverture de la succession</h2>
      <p class="article-content">
        La succession s’ouvre au décès du défunt, au dernier domicile duquel il était domicilié. Les héritiers sont appelés à recueillir le patrimoine selon les règles légales ou testamentaires.
      </p>
    </article>

    <article>
      <h2>Article 913 - Réserve héréditaire</h2>
      <p class="article-content">
        Une part minimale de la succession est réservée aux héritiers réservataires (enfants ou conjoint survivant), qui ne peuvent être totalement déshérités sauf exceptions prévues par la loi.
      </p>
    </article>

