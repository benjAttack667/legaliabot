    <article>
      <h2>Article L210-1 - Définition de la société</h2>
      <p class="article-content">
        La société est instituée par deux ou plusieurs personnes qui conviennent par un contrat d'affecter à une entreprise commune des biens ou leur industrie en vue de partager le bénéfice ou de profiter de l'économie qui pourra en résulter. Elle peut être instituée, dans les cas prévus par la loi, par l'acte de volonté d'une seule personne.
      </p>
    </article>

    <article>
      <h2>Article L225-129 - Augmentation de capital</h2>
      <p class="article-content">
        L'augmentation du capital social peut être réalisée par émission de titres nouveaux ou par élévation du montant nominal des titres existants. Elle doit être décidée par l'assemblée générale extraordinaire des actionnaires.
      </p>
    </article>

    <article>
      <h2>Article L123-12 - Comptabilité des commerçants</h2>
      <p class="article-content">
        Toute personne physique ou morale ayant la qualité de commerçant doit procéder à l'enregistrement comptable des mouvements affectant le patrimoine de son entreprise. Elle doit établir des comptes annuels à la clôture de chaque exercice.
      </p>
    </article>

