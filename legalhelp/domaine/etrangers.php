    <h1>Droit des Étrangers</h1>
    <p>Extraits essentiels du Code de l'entrée et du séjour des étrangers et du droit d'asile</p>

    <article>
      <h2>Article L111-1 - Conditions d'entrée et de séjour</h2>
      <p class="article-content">
        L'entrée et le séjour des étrangers en France sont soumis à des conditions déterminées par la loi, notamment la possession d'un visa ou d'un titre de séjour valable.
      </p>
    </article>

    <article>
      <h2>Article L311-1 - Carte de séjour temporaire</h2>
      <p class="article-content">
        La carte de séjour temporaire est délivrée pour une durée maximale d'un an, renouvelable, à des étrangers qui justifient d'un motif légal, tel que travail, études, regroupement familial.
      </p>
    </article>

    <article>
      <h2>Article L314-11 - Naturalisation</h2>
      <p class="article-content">
        La naturalisation française peut être accordée à un étranger remplissant les conditions de résidence, d'intégration et de connaissance de la langue française.
      </p>
    </article>

    <article>
      <h2>Article L521-1 - Demande d'asile</h2>
      <p class="article-content">
        Toute personne craignant des persécutions peut demander l'asile. La demande est examinée par l'Office français de protection des réfugiés et apatrides (OFPRA).
      </p>
    </article>

