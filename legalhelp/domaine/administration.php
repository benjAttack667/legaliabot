    <article>
      <h2>Article L911-1 - Compétence du tribunal administratif</h2>
      <p class="article-content">
        Le tribunal administratif est compétent pour connaître des litiges relatifs aux actes des autorités administratives, des contrats publics, et des décisions individuelles prises par les administrations.
      </p>
    </article>

    <article>
      <h2>Article L211-1 - Fonction publique d'État</h2>
      <p class="article-content">
        La fonction publique de l'État comprend les agents employés par l'État dans ses services administratifs, techniques, scientifiques, culturels, et éducatifs, soumis à un statut particulier.
      </p>
    </article>

    <article>
      <h2>Article L123-1 - Responsabilité administrative</h2>
      <p class="article-content">
        L'administration est responsable des dommages causés par ses agents dans l'exercice de leurs fonctions, sous réserve des règles spécifiques de la responsabilité administrative.
      </p>
    </article>

